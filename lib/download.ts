export function solutionToMarkdown(s: ArchitectureSolution): string {
  const lines: string[] = [];
  const push = (str = "") => lines.push(str);

  push(`# Enterprise Architecture Proposal`);
  push();
  push(`> Generated by AI Solution Architect — ${s.meta.provider} (${s.meta.model}) on ${formatDate(s.meta.generatedAt)}.${s.meta.focus ? ` Focus: ${s.meta.focus}.` : ""}`);
  push();
  push(`---`);
  push();

  push(`## 1. Executive Summary`);
  push();
  push(s.executiveSummary);
  push();

  push(`## 2. Proposed Architecture`);
  push();
  for (const { label, key } of COMPONENT_FIELDS) {
    push(`### ${label}`);
    push();
    push(s.components[key]);
    push();
  }

  push(`## 3. Architecture Diagram`);
  push();
  push("```mermaid");
  push(stripMermaidFences(s.mermaidDiagram));
  push("```");
  push();

  push(`## 4. Design Decisions`);
  push();
  for (const d of s.designDecisions) {
    push(`### ${d.component}`);
    push();
    push(d.rationale);
    push();
    push(`**Advantages**`);
    for (const a of d.advantages) push(`- ${a}`);
    push();
    push(`**Trade-offs**`);
    for (const t of d.tradeOffs) push(`- ${t}`);
    push();
  }

  push(`## 5. Risks`);
  push();
  push(`| ID | Category | Description | Severity | Likelihood |`);
  push(`| --- | --- | --- | --- | --- |`);
  for (const r of s.risks) {
    push(`| ${r.id} | ${r.category} | ${r.description} | ${r.severity} | ${r.likelihood} |`);
  }
  push();

  push(`## 6. Mitigations`);
  push();
  for (const m of s.mitigations) {
    push(`### ${m.riskId} — ${m.strategy}`);
    push();
    for (const a of m.actions) push(`- ${a}`);
    push();
  }

  push(`## 7. Deployment Recommendation`);
  push();
  push(`**Pattern:** ${s.deployment.pattern}`);
  push();
  push(s.deployment.rationale);
  push();
  push(`**Topology**`);
  push();
  s.deployment.topology.forEach((step, i) => push(`${i + 1}. ${step}`));
  push();
  push(`**DR Objective:** RTO ${s.deployment.drObjective.rto} / RPO ${s.deployment.drObjective.rpo}`);
  push();

  return lines.join("\n");
}

export function downloadMarkdown(s: ArchitectureSolution): void {
  const md = solutionToMarkdown(s);
  triggerDownload(new Blob([md], { type: "text/markdown;charset=utf-8" }), "architecture-proposal.md");
}

