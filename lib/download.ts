function escapeHtml(text: string): string {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

/** Minimal markdown -> HTML for bold, inline code, lists, and paragraphs. */
function mdToHtml(text: string): string {
  const escaped = escapeHtml(text);
  const lines = escaped.split("\n");
  const out: string[] = [];
  let inList = false;

  const closeList = () => {
    if (inList) {
      out.push("</ul>");
      inList = false;
    }
  };

  for (const raw of lines) {
    const line = raw.trim();
    if (line.startsWith("- ") || line.startsWith("* ")) {
      if (!inList) {
        out.push("<ul>");
        inList = true;
      }
      out.push(`<li>${inline(line.slice(2))}</li>`);
    } else if (line === "") {
      closeList();
    } else {
      closeList();
      out.push(`<p>${inline(line)}</p>`);
    }
  }
  closeList();
  return out.join("\n");
}

function inline(text: string): string {
  return text
    .replace(/\*\*([^*]+)\*\*/g, "<strong>$1</strong>")
    .replace(/`([^`]+)`/g, "<code>$1</code>");
}

function stripMermaidFences(code: string): string {
  const m = code.trim().match(/```(?:mermaid)?\s*([\s\S]*?)```/i);
  return (m?.[1] ?? code).trim();
}

function severityClass(sev: string): string {
  switch (sev) {
    case "Critical":
      return "sev-critical";
    case "High":
      return "sev-high";
    case "Medium":
      return "sev-medium";
    default:
      return "sev-low";
  }
}

